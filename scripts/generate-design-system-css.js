#!/usr/bin/env node
/**
 * Design System CSS 產生器（build-time Node script）
 *
 * 業務情境（0.19.1-W2-001 D1 決策）：
 *   design-system Phase 1 已出貨 JS 常數，但 CSS/HTML 全硬編碼，三套主色並存。
 *   D1 定案 colors.js / spacing.js / typography.js 為單一來源（SSOT），
 *   design-system.css 為 generated artifact。本 script 讀 3 個 JS 模組，
 *   產出 :root CSS 變數（camelCase JS key → kebab-case CSS var），對齊
 *   docs/spec/design-system-spec.md Section 10 CSS Custom Properties。
 *
 * 為何是 build-time Node script：產生器在 Node 環境執行（非 Chrome Extension
 *   runtime），可使用 require() 載入 3 個 design-system JS 模組。產出的
 *   design-system.css 才是注入到 4 個 CE runtime 的最終產物。
 *
 * 防漂移設計：
 *   - 產物檔頭標 AUTO-GENERATED DO NOT EDIT，禁手改。
 *   - npm run validate:design-system 重新產生 + git diff --exit-code，
 *     不一致則 CI fail（產物與 3 個 .js 來源脫鉤即攔截）。
 *   - tests/unit/design-system-css-snapshot.test.js 斷言關鍵 token 存在。
 *
 * @see docs/work-logs/.../0.19.1-W2-001.md Solution 第 1 節（:root 變數映射）
 */

const fs = require('fs')
const path = require('path')

const { COLORS, STATUS_COLORS } = require('../src/core/design-system/colors.js')
const { SPACING, BORDER_RADIUS } = require('../src/core/design-system/spacing.js')
const { FONT_FAMILY, FONT_SIZES, FONT_WEIGHTS } = require('../src/core/design-system/typography.js')
const { SHADOW_COLORS, OVERLAY_COLORS } = require('../src/core/design-system/shadows.js')

const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'core', 'design-system', 'design-system.css')

const AUTO_GENERATED_HEADER =
  '/* AUTO-GENERATED by scripts/generate-design-system-css.js — DO NOT EDIT */'

/**
 * camelCase 轉 kebab-case
 *
 * 範例：primaryLightest → primary-lightest、bodyLarge → body-large、
 *       semiBold → semi-bold、xxs → xxs（無大寫不變）。
 * 規則：大寫字母前插入連字號並轉小寫；連續大寫（罕見於本專案 key）逐一處理。
 *
 * @param {string} key - camelCase 識別符
 * @returns {string} kebab-case 識別符
 */
function camelToKebab (key) {
  return key.replace(/([a-z0-9])([A-Z])/g, '$1-$2').toLowerCase()
}

/**
 * 將一組 { key: value } 映射為 CSS 變數行陣列
 *
 * @param {string} prefix - CSS 變數前綴（如 'color'、'spacing'）
 * @param {object} mapping - 來源常數物件
 * @param {function} formatValue - 值格式化函式（如數字加 'px'）
 * @returns {string[]} CSS 變數行（含縮排）
 */
function mapToCssVars (prefix, mapping, formatValue) {
  return Object.keys(mapping).map((key) => {
    const varName = `--${prefix}-${camelToKebab(key)}`
    return `  ${varName}: ${formatValue(mapping[key])};`
  })
}

const asIs = (v) => v
const asPx = (v) => `${v}px`

/**
 * 產出 design-system.css 的完整內容字串
 *
 * 區塊順序對齊 spec Section 2/3/5/6/7。每區塊先註解標明 spec section，
 * 再列出 :root 變數。狀態徽章色（Section 3）為 D3 修正後的深字淺底配色。
 *
 * @returns {string} 完整 CSS 檔內容（含尾端換行）
 */
function buildCss () {
  const lines = []

  lines.push(AUTO_GENERATED_HEADER)
  lines.push(':root {')

  // Colors (Section 2)
  lines.push('  /* Colors (Section 2) */')
  lines.push(...mapToCssVars('color', COLORS, asIs))
  lines.push('')

  // Status Badge Colors (Section 3, D3 修正後)
  lines.push('  /* Status Badge Colors (Section 3, D3 修正後) */')
  Object.keys(STATUS_COLORS).forEach((status) => {
    const { fg, bg } = STATUS_COLORS[status]
    lines.push(`  --status-${camelToKebab(status)}-fg: ${fg};`)
    lines.push(`  --status-${camelToKebab(status)}-bg: ${bg};`)
  })
  lines.push('')

  // Spacing (Section 5)
  lines.push('  /* Spacing (Section 5) */')
  lines.push(...mapToCssVars('spacing', SPACING, asPx))
  lines.push('')

  // Border Radius (Section 6)
  lines.push('  /* Border Radius (Section 6) */')
  lines.push(...mapToCssVars('radius', BORDER_RADIUS, asPx))
  lines.push('')

  // Typography (Section 7)
  lines.push('  /* Typography (Section 7) */')
  lines.push(`  --font-family: ${FONT_FAMILY};`)
  lines.push(...mapToCssVars('font-size', FONT_SIZES, asPx))
  lines.push(...mapToCssVars('font-weight', FONT_WEIGHTS, asIs))
  lines.push('')

  // Shadows & Overlay (Section 8) — 中性純黑陰影顏色與遮罩 scrim
  lines.push('  /* Shadows & Overlay (Section 8) */')
  lines.push(...mapToCssVars('shadow-color', SHADOW_COLORS, asIs))
  lines.push(...mapToCssVars('overlay', OVERLAY_COLORS, asIs))

  lines.push('}')
  lines.push('')

  return lines.join('\n')
}

/**
 * 產生 design-system.css 並寫入 OUTPUT_PATH
 *
 * 由 build.js（dev/prod 流程開頭）與 npm run validate:design-system 呼叫。
 * 寫入前後內容皆以 buildCss() 為單一來源，確保 validate 的 git diff 可靠。
 *
 * @returns {{outputPath: string, content: string}} 寫入路徑與內容
 */
function generate () {
  const content = buildCss()
  fs.writeFileSync(OUTPUT_PATH, content, 'utf8')
  return { outputPath: OUTPUT_PATH, content }
}

// 直接執行時產生 CSS；require 時僅匯出純函式供測試與 build.js 接線。
if (require.main === module) {
  const { outputPath } = generate()
  // eslint-disable-next-line no-console -- build-time CLI 進度回饋，與 scripts/build.js 同模式
  console.log(`[DESIGN-SYSTEM] 已產生 ${path.relative(path.join(__dirname, '..'), outputPath)}`)
}

module.exports = { generate, buildCss, camelToKebab, OUTPUT_PATH }
